#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –∏ –¥–µ–ø–ª–æ—è –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –Ω–∞ —Å–µ—Ä–≤–µ—Ä VK Cloud —Å GitHub
# –ë–µ–∑–æ–ø–∞—Å–Ω–∞—è –≤–µ—Ä—Å–∏—è - —á—É–≤—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –∑–∞–ø—Ä–∞—à–∏–≤–∞—é—Ç—Å—è –∏–ª–∏ –±–µ—Ä—É—Ç—Å—è –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è

# –ó–∞–≥—Ä—É–∑–∫–∞ —á—É–≤—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö –∏–∑ —Ñ–∞–π–ª–∞, –µ—Å–ª–∏ –æ–Ω —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
if [ -f ".env.deploy" ]; then
  source .env.deploy
fi

# –ó–∞–ø—Ä–æ—Å –¥–∞–Ω–Ω—ã—Ö, –µ—Å–ª–∏ –æ–Ω–∏ –Ω–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω—ã
if [ -z "$SERVER_USER" ]; then
  read -p "–í–≤–µ–¥–∏—Ç–µ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å–µ—Ä–≤–µ—Ä–∞: " SERVER_USER
fi

if [ -z "$SERVER_IP" ]; then
  read -p "–í–≤–µ–¥–∏—Ç–µ IP-–∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞: " SERVER_IP
fi

if [ -z "$SERVER_PORT" ]; then
  read -p "–í–≤–µ–¥–∏—Ç–µ –ø–æ—Ä—Ç SSH (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 22): " SERVER_PORT
  SERVER_PORT=${SERVER_PORT:-22}
fi

if [ -z "$GITHUB_REPO" ]; then
  read -p "–í–≤–µ–¥–∏—Ç–µ URL GitHub —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è: " GITHUB_REPO
fi

if [ -z "$REMOTE_DIR" ]; then
  read -p "–í–≤–µ–¥–∏—Ç–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é /home/$SERVER_USER/article-generator): " REMOTE_DIR
  REMOTE_DIR=${REMOTE_DIR:-/home/$SERVER_USER/article-generator}
fi

# –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –±—É–¥—É—â–µ–≥–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è
read -p "–°–æ—Ö—Ä–∞–Ω–∏—Ç—å —ç—Ç–∏ –¥–∞–Ω–Ω—ã–µ –¥–ª—è –±—É–¥—É—â–∏—Ö –∑–∞–ø—É—Å–∫–æ–≤? (y/n): " SAVE_DATA
if [ "$SAVE_DATA" = "y" ]; then
  echo "SERVER_USER=\"$SERVER_USER\"" > .env.deploy
  echo "SERVER_IP=\"$SERVER_IP\"" >> .env.deploy
  echo "SERVER_PORT=\"$SERVER_PORT\"" >> .env.deploy
  echo "GITHUB_REPO=\"$GITHUB_REPO\"" >> .env.deploy
  echo "REMOTE_DIR=\"$REMOTE_DIR\"" >> .env.deploy
  echo "# –î–æ–±–∞–≤–ª–µ–Ω –≤ .gitignore, –Ω–µ –∫–æ–º–º–∏—Ç—å—Ç–µ —ç—Ç–æ—Ç —Ñ–∞–π–ª" >> .env.deploy
  
  # –î–æ–±–∞–≤–ª—è–µ–º —Ñ–∞–π–ª –≤ .gitignore, –µ—Å–ª–∏ –µ–≥–æ —Ç–∞–º –µ—â–µ –Ω–µ—Ç
  if ! grep -q ".env.deploy" .gitignore; then
    echo ".env.deploy" >> .gitignore
  fi
fi

echo "üöÄ –ù–∞—á–∏–Ω–∞–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫—É –∏ –¥–µ–ø–ª–æ–π –Ω–∞ —Å–µ—Ä–≤–µ—Ä"

# –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ —Å–µ—Ä–≤–µ—Ä—É –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞/–æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
echo "üîÑ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ..."
ssh -p $SERVER_PORT $SERVER_USER@$SERVER_IP << EOF
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ª–∏ Git –∏ Python
    command -v git >/dev/null 2>&1 || { echo "‚öôÔ∏è –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º Git..."; sudo apt update && sudo apt install -y git; }
    command -v python3 >/dev/null 2>&1 || { echo "‚öôÔ∏è –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º Python..."; sudo apt update && sudo apt install -y python3 python3-pip python3-venv; }
    
    # –°–æ–∑–¥–∞–µ–º –∏–ª–∏ –æ–±–Ω–æ–≤–ª—è–µ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π
    if [ ! -d "$REMOTE_DIR" ]; then
        echo "üìÅ –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è..."
        git clone $GITHUB_REPO $REMOTE_DIR
    else
        echo "üîÑ –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è..."
        cd $REMOTE_DIR
        git pull
    fi
    
    # –°–æ–∑–¥–∞–µ–º –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –µ—Å–ª–∏ –µ–≥–æ –Ω–µ—Ç
    cd $REMOTE_DIR
    if [ ! -d "venv" ]; then
        echo "üîß –°–æ–∑–¥–∞–Ω–∏–µ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–π —Å—Ä–µ–¥—ã Python..."
        python3 -m venv venv
    fi
    
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏
    echo "üì¶ –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π..."
    source venv/bin/activate
    pip install -r requirements.txt
    
    # –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –¥–ª—è –ª–æ–≥–æ–≤
    mkdir -p logs
    
    # –ö–æ–ø–∏—Ä—É–µ–º .env.example –≤ .env –µ—Å–ª–∏ .env –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
    if [ ! -f "config/.env" ] && [ -f "config/.env.example" ]; then
        echo "‚öôÔ∏è –°–æ–∑–¥–∞–Ω–∏–µ —Ñ–∞–π–ª–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ –∏–∑ —à–∞–±–ª–æ–Ω–∞..."
        cp config/.env.example config/.env
        echo "‚ùó –ù–µ –∑–∞–±—É–¥—å—Ç–µ –æ—Ç—Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞—Ç—å config/.env –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ API –∫–ª—é—á–µ–π!"
    fi
    
    # –°–æ–∑–¥–∞–µ–º –∏ –Ω–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º systemd —Å–µ—Ä–≤–∏—Å –µ—Å–ª–∏ –µ–≥–æ –Ω–µ—Ç
    if [ ! -f "/etc/systemd/system/article-generator.service" ]; then
        echo "üîß –ù–∞—Å—Ç—Ä–æ–π–∫–∞ systemd —Å–µ—Ä–≤–∏—Å–∞..."
        sudo bash -c 'cat > /etc/systemd/system/article-generator.service << EOFSERVICE
[Unit]
Description=Article Generator Service
After=network.target

[Service]
User=$USER
WorkingDirectory=$REMOTE_DIR
Environment="PATH=$REMOTE_DIR/venv/bin"
ExecStart=$REMOTE_DIR/venv/bin/python src/main.py
Restart=always

[Install]
WantedBy=multi-user.target
EOFSERVICE'

        sudo systemctl daemon-reload
        sudo systemctl enable article-generator
    fi
    
    # –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º —Å–µ—Ä–≤–∏—Å
    echo "üîÑ –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫ —Å–µ—Ä–≤–∏—Å–∞..."
    sudo systemctl restart article-generator
    echo "‚úÖ –°–µ—Ä–≤–∏—Å –ø–µ—Ä–µ–∑–∞–ø—É—â–µ–Ω"
    sleep 2
    sudo systemctl status article-generator | grep "Active"
EOF

echo "‚úÖ –î–µ–ø–ª–æ–π –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ"
echo "üìù –ù–µ –∑–∞–±—É–¥—å—Ç–µ –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ –≤ —Ñ–∞–π–ª–µ config/.env" 